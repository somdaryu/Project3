package quiz;

public class Quiz1 {

	public static void main(String[] args) {
		
		int num = -10; // -10
		int result = +num; // -10 +연산자는 부호 그대로 - 연산자는 부호 반대로
		
		int num2 = -20; // -20
		int result2 = -num2; // 20
		
		System.out.println(num);
		System.out.println(result);
		System.out.println(num2);
		System.out.println(result2);
	}

}
