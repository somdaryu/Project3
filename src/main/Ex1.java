package main;

public class Ex1 {

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = -20;
		
		System.out.println(+num1);
		System.out.println(+num2);
		
		System.out.println(-num1);
		System.out.println(-num2);

		//부호연산자는 값 자체를 바꾸지 않고, 값을 카피해서 사용함
		System.out.println(num1);
		System.out.println(num2);
		
	}

}
